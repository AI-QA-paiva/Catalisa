package com.calculadora;

public class Calculadora {

    public int somar(int a, int b){
        return a + b;
    }

    public double multiplicar(double c, double d){
        return c * d;
    }

    double subtrair(double e, double f){
        return e - f;
    }

    double dividir(double g,double h){
        g = multiplicar(2,2);
        return g/h;
    }


}


